                    <li>
                        <a data-toggle="collapse" class="collapsed" href="#faq1">1. What is color blindness ? <i class="fa fa-minus-circle"></i></a>
                        <div id="faq1" class="collapse" data-parent="#faq-list">
                            <p>
                                Color blindness is a biological condition, where you can’t distinguish the same broad spectrum of colors as somebody with normal color vision. The term stands for anything between almost normal color vision and the most pronounced form, where you can only differentiate shades of gray.
                            </p>
                        </div>
                    </li>

                    <li>
                        <a data-toggle="collapse" href="#faq2" class="collapsed">2. Who is mainly color blind, males or females ? <i class="fa fa-minus-circle"></i></a>
                        <div id="faq2" class="collapse" data-parent="#faq-list">
                            <p>
                                Mainly males suffer from color vision deficiency. This is because the most common form of color blindness is provoked by some variations on the X chromosome—also called sex chromosome. Because women have two such chromosomes, they have the chance to overmodulate the defective one. On the other side, men have only one X combined with a Y chromosome and therefore suffer more often form color blindness.
                            </p>
                        </div>
                    </li>

                    <li>
                        <a data-toggle="collapse" href="#faq3" class="collapsed">3. How does an optician or an ophthalmologist test for color blindness ? <i class="fa fa-minus-circle"></i></a>
                        <div id="faq3" class="collapse" data-parent="#faq-list">
                            <p>
                                They only have some Ishihara plates to test colorblind persons. For proper testing you also would need some other tests like an University test for blue-yellow color blindness and of course an anomaloscope to get an accurate test on red-green color blindness.                            </p>
                        </div>
                    </li>

                    <li>
                        <a data-toggle="collapse" href="#faq4" class="collapsed">4. At what age should people be tested for their ability to see colors ? <i class="fa fa-minus-circle"></i></a>
                        <div id="faq4" class="collapse" data-parent="#faq-list">
                            <p>
                                Parents should be aware of color blindness specially with boys, because almost 10% of all men have some type of color vision deficiency. If you think that there could be a problem you might test it in kindergarten, because if you know about it, you and the teachers can give better support to your child. On the other side if you are not sure how good your color vision is, you can test it before you decide on your later profession, because there could arise some major problems.                            </p>
                        </div>
                    </li>

                    <li>
                        <a data-toggle="collapse" href="#faq5" class="collapsed">5. Is there a reason that color blindness is important to diagnose ? Why ? <i class="fa fa-minus-circle"></i></a>
                        <div id="faq5" class="collapse" data-parent="#faq-list">
                            <p>
                                First of all, I don’t think it should be tested in general. Only if you have some concerns a test can help you very much to classify them. It is important to diagnose color blindness, because only then you can take proper precautions to help for example your child with homework.                            </p>
                        </div>
                    </li>

                    <li>
                        <a data-toggle="collapse" href="#faq6" class="collapsed">6. Are there treatments to cure color blindness ? <i class="fa fa-minus-circle"></i></a>
                        <div id="faq6" class="collapse" data-parent="#faq-list">
                            <p>
                                No, there are no treatments for any color vision deficiency. There are some lenses, which try to enhance the color vision but otherwise you just have to get around with it. Maybe in the future there will be some possibilities in genetic manipulations, but there is still a very long way to go.                            </p>
                        </div>
                    </li>

                    <li>
                        <a data-toggle="collapse" href="#faq6" class="collapsed">7. Are you aware of any specific jobs that color-blind people could not perform ? <i class="fa fa-minus-circle"></i></a>
                        <div id="faq6" class="collapse" data-parent="#faq-list">
                            <p>
                                The most talked about job which can not be taken by a colorblind person is pilot. Because pilots have a big responsibility and almost everything in a cockpit and outside works with some color coded signals, it wouldn’t be a good choice of job if you have some kind of color vision deficiency. There are also problems to apply for police officer, and you might have some disadvantages in biology/chemistry and as an electrician, because of color coded wires.                        </div>
                    </li>

