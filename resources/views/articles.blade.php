                        <h4>Different kinds of color blindness<span>Types & terms used</span></h4>
                        <p>Based on clinical appearance, color blindness may be described as total or partial.
                            Total color blindness is much less common than partial color blindness. There are two major
                            types of color blindness: difficulty distinguishing between red and green, and difficulty
                            distinguishing between blue and yellow.
                            Immunofluorescent imaging is a way to determine red-green color coding. Conventional color coding is
                            difficult for individuals with red–green color blindness (protanopia or deuteranopia) to discriminate.
                            Replacing red with magenta or green with turquoise improves visibility for such individuals. </p>
                    </div>
                </div>
                <div class="row schedule-item">
                    <div class="col-md-10">
                        <p>The different kinds of inherited color blindness result from partial or complete loss of function of one or
                            more of the three different cone systems. When one cone system is compromised, dichromacy results. The most frequent
                            forms of human color blindness result from problems with either the middle (green) or long (red) wavelength sensitive
                            cone systems, and make it hard to discriminate reds, yellows, and greens from one another. They are collectively
                            referred to as "red-green color blindness", though the term is an over-simplification and is somewhat misleading.
                            Other forms of color blindness are much more rare. They include problems in discriminating blues from greens and yellows
                            from reds/pinks, and the rarest form of all, complete color blindness or monochromacy, where one cannot distinguish
                            any color from grey, as in a black-and-white movie or photograph.</p>
                    </div>
                </div>

                <div class="row schedule-item">
                    <div class="col-md-10">
                        <p>
                            Protanopes, deuteranopes, and tritanopes are dichromats; that is, they can match any color
                            they see with some mixture of just two primary colors (in contrast to those with normal sight
                            (trichromats) who can distinguish three primary colors). Dichromats usually know they have a
                            color vision problem, and it can affect their daily lives. Out of the male population, 2% have
                            severe difficulties distinguishing between red, orange, yellow, and green. (Orange and yellow are
                            different combinations of red and green light.) Colors in this range, which appear very different
                            to a normal viewer, appear to a dichromat to be the same or a similar color. The terms protanopia,
                            deuteranopia, and tritanopia come from Greek, and respectively mean "inability to see (anopia) with
                            the first (prot-), second (deuter-), or third (trit-)".
                        </p>
                    </div>
                </div>
                <div class="row schedule-item">
                    <div class="col-md-10">
                        <p>
                            Anomalous trichromacy is the least serious type of color deficiency. People with protanomaly,
                            deuteranomaly, or tritanomaly are trichromats, but the color matches they make differ from the normal.
                            They are called anomalous trichromats. In order to match a given spectral yellow light,
                            protanomalous observers need more red light in a red/green mixture than a normal observer,
                            and deuteranomalous observers need more green. From a practical standpoint though, many protanomalous
                            and deuteranomalous people have very little difficulty carrying out tasks that require normal color
                            vision. Some may not even be aware that their color perception is in any way different from normal.
                        </p>
                    </div>
                </div>
