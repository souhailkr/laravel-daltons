                        <h4>Families</h4>
                        <p><Strong>Red-green color blindness</Strong></p>
                        <p>
                            Protanopia, deuteranopia, protanomaly, and deuteranomaly are commonly inherited forms
                            of red-green color blindness which affect a substantial portion of the human population.
                            Those affected have difficulty with discriminating red and green hues due to the absence or
                            mutation of the red or green retinal photoreceptors. It is sex-linked: genetic red-green color
                            blindness affects males much more often than females, because the genes for the red and green color
                            receptors are located on the X chromosome, of which males have only one and females have two. Females
                            (XX) are red-green color blind only if both their X chromosomes are defective with a similar deficiency,
                            whereas males (XY) are color blind if their single X chromosome is defective.
                        </p>
                        <br>
                        <p><strong>Blue-yellow color blindness</strong></p>
                        <p>
                            Actually the wording blue-yellow color blindness is misleading. People affected by tritan color
                            blindness confuse blue with green and yellow with violet. So the term blue-green color blindness
                            would be more accurate because the colors blue and yellow are usually not mixed up by tritanopes.
                            Blue-yellow color blindness can be observed only very rarely. Different studies diverge a lot in the
                            numbers but as a rule of thumb you could say one out of 10’000 persons is affected at most. In contrary
                            to red-green color blindness tritan defects are autosomal and encoded on chromosome 7. This means tritanopia
                            and tritanomaly are not sex-linked traits and therefore women and men are equally affected.
                        </p>
                        <br>
                        <p><strong>Total color blindness</strong></p>
                        <p>
                            Total color blindness is defined as the inability to see color. Although the term may refer
                            to acquired disorders such as cerebral achromatopsia also known as color agnosia, it typically
                            refers to congenital color vision disorders (i.e. more frequently rod monochromacy and less frequently
                            cone monochromacy).
                            In cerebral achromatopsia, a person cannot perceive colors even though the eyes are capable of
                            distinguishing them. Some sources do not consider these to be true color blindness,
                            because the failure is of perception, not of vision. They are forms of visual agnosia
                        </p>

