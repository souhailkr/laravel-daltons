                        <p>Violet, lavender, and purple are indistinguishable from various shades of blue because their reddish components are so dimmed as to be invisible. For example, pink flowers, reflecting both red light and blue light, may appear just blue to the protanope. A very few people have been found who have one normal eye and one protanopic eye. These unilateral dichromats report that with only their protanopic eye open, they see wavelengths shorter than neutral point as blue and those longer than it as yellow. This is a rare form of color blindness.</p>
                    </div>
                </div>

                <div class="row schedule-item">
                    <div class="col-md-10">
                        <div class="speaker">
                            <img src="img/protanopia/3.jpg" alt="Spectrum">
                        </div>
                        <h4>Causes</h4>
                        <p>Protanopia is caused by the complete absence of red retinal photoreceptors (L-cones are missing at all). Protans have difficulties distinguishing between blue and green colors and also between red and green colors. It is a form of dichromatism in which the subject can only perceive light wavelengths from 400 nm to 650 nm, instead of the usual 700 nm. Pure reds cannot be seen, instead appearing black; purple colors cannot be distinguished from blues; more orange-tinted reds may appear as dim yellows, and all orange-yellow-green shades of too long a wavelength to stimulate the blue receptors appear as a similar yellow hue. It is present in 1% of males.</p>
                    </div>
                </div>

                <div class="row schedule-item">
                    <div class="col-md-10">
