                            Dichromats often confuse red and green items. For example, they may find it difficult to
                            distinguish a Braeburn apple from a Granny Smith or red from green of traffic lights without
                            other clues—for example, shape or position. Dichromats tend to learn to use texture and shape
                            clues and so may be able to penetrate camouflage that has been designed to deceive individuals
                            with normal color vision.
                        </p>
