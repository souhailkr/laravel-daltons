                        <h4>Diagnosis <span>Ishihara Test</span></h4>
                        <p>In protanopia, the visible range of the spectrum is shorter at the red end compared with that of the normal, and that part of the spectrumwhich appears to the normal as blue-green, appears to those with protanopia as grey. The whole visible range of the spectrum in protanopia consists of two areas which are separated from each other by this grey part. Each areas appears to those with protanopia as one system of colour with different brightness and saturation within each area being different from that of the other. The red with a slight tinge of purple which is the complementary colour of blue-green appears also as grey.</p>
                    </div>
                </div>

                <div class="row schedule-item">
                    <div class="col-md-10">
                        <div class="speaker">
