                        <h4>Definition <span>  1% of males</span></h4>
                        <p>Protans have either defective long-wavelength cones or the L-cones are missing at all. Lacking the red cones for long-wavelength sensitive retinal cones, those with this condition are unable to distinguish between colors in the green-yellow-red section of the spectrum.</p>
                    </div>
