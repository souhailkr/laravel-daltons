                        <h4>Explanation</h4>
                        <p> They have a neutral point at a cyan-like wavelength around 492 nm (see spectral color for comparison)—that is, they cannot discriminate light of this wavelength from white. For a protanope, the brightness of red, orange, and yellow are much reduced compared to normal. This dimming can be so pronounced that reds may be confused with black or dark gray, and red traffic lights may appear to be extinguished. They may learn to distinguish reds from yellows primarily on the basis of their apparent brightness or lightness, not on any perceptible hue difference</p>
                    </div>
                </div>

                <div class="row schedule-item">
                    <div class="col-md-10">
